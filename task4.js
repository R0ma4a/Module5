function calculateArea(length, width){
    return length * width;
}

console.log("The area of a 5x4 rectangle is: " + calculateArea(5, 4));

console.log("The area of a 1x1 rectangle is: " + calculateArea(1, 1));

console.log("The area of a 6x6 rectangle is: " + calculateArea(6, 6));

console.log("The area of a 7.5x4.3 rectangle is: " + calculateArea(7.5, 4.3));